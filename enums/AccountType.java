package enums;

/**
 * Enum representing different types of financial accounts.
 * This enum includes the following account types:
 * - BANK: Represents a bank account.
 * - WALLET: Represents a digital wallet account.
 */
public enum AccountType {
    /**
     * Represents a traditional bank account.
     */
    BANK,

    /**
     * Represents a digital wallet account.
     */
    WALLET
}
