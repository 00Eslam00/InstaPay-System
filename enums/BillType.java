package enums;

/**
 * Enum representing different types of bills.
 * This enum includes the following bill types:
 * - GAS: Represents a gas bill.
 * - ELECTRICITY: Represents an electricity bill.
 * - WATER: Represents a water bill.
 */
public enum BillType {
    /**
     * Represents a gas bill.
     */
    GAS,

    /**
     * Represents an electricity bill.
     */
    ELECTRICITY,

    /**
     * Represents a water bill.
     */
    WATER
}
